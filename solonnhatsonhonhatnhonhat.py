a = int(input("Nhập số nguyên thứ nhất: "))
b = int(input("Nhập số nguyên thứ hai: "))
c = int(input("Nhập số nguyên thứ ba: "))
solonnhat = max(a, b, c )
sonhonhat = min(a, b, c )
print("Số nguyên lớn nhất là: ", solonnhat)
print("Số nguyên nhỏ nhất là: ", sonhonhat)
